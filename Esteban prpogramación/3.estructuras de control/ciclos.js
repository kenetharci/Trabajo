"use strict";
//while, do while, for
let i=0;
while(i < 10){
    console.log(i);
    i++;
}
console.log("**************************************************");
let j=10;
do{
    console.log(j);
    j--;  
}while(j > 0);
console.log("**************************************************");
for(let k=0; k <5; k++){
    console.log(k);
}